package com.invesco.datamigration.vo;

public class AttachmentDetails {
	
	private DocumentDetails documentDetails = null;
	private String attachmentSourceName=null;
	private String attachmentSourcePath=null;
	private String attachmentDestName=null;
	private String attachmentDestPath=null;
	private String processingStatus=null;
	private String errorMessage=null;
	
	public DocumentDetails getDocumentDetails() {
		return documentDetails;
	}
	public void setDocumentDetails(DocumentDetails documentDetails) {
		this.documentDetails = documentDetails;
	}
	public String getAttachmentSourceName() {
		return attachmentSourceName;
	}
	public void setAttachmentSourceName(String attachmentSourceName) {
		this.attachmentSourceName = attachmentSourceName;
	}
	public String getAttachmentSourcePath() {
		return attachmentSourcePath;
	}
	public void setAttachmentSourcePath(String attachmentSourcePath) {
		this.attachmentSourcePath = attachmentSourcePath;
	}
	public String getAttachmentDestName() {
		return attachmentDestName;
	}
	public void setAttachmentDestName(String attachmentDestName) {
		this.attachmentDestName = attachmentDestName;
	}
	public String getAttachmentDestPath() {
		return attachmentDestPath;
	}
	public void setAttachmentDestPath(String attachmentDestPath) {
		this.attachmentDestPath = attachmentDestPath;
	}
	public String getProcessingStatus() {
		return processingStatus;
	}
	public void setProcessingStatus(String processingStatus) {
		this.processingStatus = processingStatus;
	}
	public String getErrorMessage() {
		return errorMessage;
	}
	public void setErrorMessage(String errorMessage) {
		this.errorMessage = errorMessage;
	}
	
	

}
